        <section>
            <h2>Què és un ERP?</h2>
            <img src="img/erp.png" alt="Sistema ERP">
            <p>Un ERP (Enterprise Resource Planning o Planificació de Recursos Empresarials) és un sistema de gestió integral que permet a les empreses centralitzar i automatitzar els seus processos operatius i administratius en una única plataforma. Aquests sistemes integren dades i funcionalitats de diferents àrees de l'empresa (com finances, recursos humans, inventari, vendes, etc.) per millorar l’eficiència i la presa de decisions.</p>
            <p>Els ERP són programari modular, basats sovint en bases de dades compartides, que eliminen la necessitat de sistemes independents per a cada departament. Exemples coneguts inclouen SAP, Oracle ERP, Microsoft Dynamics 365 i Odoo.</p>
        </section>
        <section>
            <h2>Per a què serveix?</h2>
            <img src="img/para_que_sirve.png" alt="Utilitat ERP">
            <ul>
                <li>Unificar la informació de tots els departaments en un sol lloc, evitant duplicitats i errors.</li>
                <li>Automatitzar processos repetitius (com facturació, gestió d’inventaris o nòmines).</li>
                <li>Millorar la traçabilitat dels processos empresarials (per exemple, seguir un producte des de la compra de matèries primeres fins a la venda).</li>
                <li>Optimitzar recursos (temps, diners i personal) gràcies a una gestió més eficient.</li>
                <li>Facilitar l’anàlisi de dades amb informes en temps real, ajudant en la presa de decisions estratègiques.</li>
            </ul>
        </section>
        <section>
            <h2>Quines funcions realitzen?</h2>
            <img src="img/modulos.jpg" alt="Mòduls ERP">
            <ul>
                <li><strong>Finances i comptabilitat:</strong> Gestió de factures, balanços, comptes a pagar/cobrar.</li>
                <li><strong>Vendes i CRM:</strong> Gestió de clients, comandes, campanyes de màrqueting.</li>
                <li><strong>Compras i cadena de subministrament:</strong> Gestió de proveïdors, sol·licituds de compra.</li>
                <li><strong>Inventari i magatzem:</strong> Control d’estocs, moviments de mercaderies.</li>
                <li><strong>Producció:</strong> Planificació de la fabricació, control de qualitat.</li>
                <li><strong>Recursos humans:</strong> Nòmines, reclutament, avaluacions.</li>
                <li><strong>Logística i distribució:</strong> Gestió de transport i entregues.</li>
            </ul>
        </section>
        <section>
            <h2>Quins tipus de mòduls hi ha? Com els classificaries? Puc instal·lar qualsevol mòdul?</h2>
            <p>Els ERP solen organitzar-se en mòduls, que es poden classificar segons l’àrea funcional que cobreixen:</p>
            <ul>
                <li><strong>Recursos Humans:</strong> Nòmines, formació, selecció.</li>
                <li><strong>Producció:</strong> Planificació, control de maquinari.</li>
                <li><strong>Logístic:</strong> Inventari, magatzem, compres.</li>
                <li><strong>Financer:</strong> Comptabilitat, pressupostos, tresoreria.</li>
                <li><strong>Comercial:</strong> Vendes, CRM, e-commerce.</li>
                <li><strong>Analítica:</strong> BI (Business Intelligence), informes.</li>
            </ul>
            <p><strong>¿Es poden instal·lar qualsevol mòdul?</strong></p>
            <p>Sí, però amb limitacions:</p>
            <ul>
                <li>Els ERP modulares permeten escollir només els mòduls necessaris (per exemple, una PIMÉ no necessita mòduls avançats de producció).</li>
                <li>Cal verificar la compatibilitat entre mòduls i si l’ERP ho permet (alguns sistemes tancats com SAP exigeixen llicències específiques).</li>
                <li>Hi ha ERP modulars i personalitzables (com Odoo) i altres més rígids (com alguns ERP industrials).</li>
            </ul>
        </section>
        <section>
            <h2>Què és la integració de mòduls i per què és beneficiosa?</h2>
            <p>La integració de mòduls significa que totes les parts del ERP comparteixen dades i es comuniquen entre si sense necessitat de sistemes externs. Per exemple, quan un venedor registra una comanda, el mòdul d’inventari actualitza automàticament les existències i finances genera la factura.</p>
            <p><strong>Beneficis:</strong></p>
            <ul>
                <li>Reducció d’errors (evita duplicar dades manualment).</li>
                <li>Estalvi de temps (automatitza fluxes de treball).</li>
                <li>Visió global en temps real de l’empresa.</li>
                <li>Escalabilitat (és fàcil afegir nous mòduls segons creix l’empresa).</li>
            </ul>
            <p><strong>Exemple pràctic:</strong></p>
            <p>Un ERP integrat evita que el departament de vendes desconegui l’estoc actual o que comptabilitat no registri una venda a temps.</p>
